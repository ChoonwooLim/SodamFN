from typing import Dict

def calculate_insurances(gross_pay: int, year: int = 2026, insurance_base: int = 0) -> Dict[str, int]:
    """
    Calculates South Korean 4 major insurance deductions based on the year.
    
    Args:
        gross_pay: Actual monthly gross pay (used as fallback if insurance_base is 0)
        year: Tax year for rate selection
        insurance_base: 보수월액 (reported monthly standard income).
                        If > 0, used for ALL 4 insurance calculations.
                        If 0, falls back to gross_pay.
    
    2026 Rates (근로자 부담분):
      - 국민연금: 4.75% (전체 9.5%, 노사 반반)
      - 건강보험: 3.595% (전체 7.19%, 노사 반반)
      - 장기요양: 13.14% of 건강보험료 (2026년 확정)
      - 고용보험: 0.9%
    2025 Rates:
      - 국민연금: 4.5%, 건강보험: 3.545%, 장기요양: 12.91%
      - 고용보험: 0.9%
    """
    if year >= 2026:
        np_rate, hi_rate, lti_multiplier = 0.0475, 0.03595, 0.1314
    else:
        # Default to 2025 rates
        np_rate, hi_rate, lti_multiplier = 0.045, 0.03545, 0.1291
    
    # Use insurance_base (보수월액) if provided, otherwise use gross_pay
    base_for_insurance = insurance_base if insurance_base > 0 else gross_pay
    
    # Cap logic for NP and HI
    np_income = max(400000, min(base_for_insurance, 6370000))
    hi_income = max(280383, min(base_for_insurance, 127725730))
    
    # 원 단위 절사 for NP (truncate to 1 won)
    # round() 사용하여 float 오차 방지 (e.g. 1500000*0.009=13499.999... → 13500)
    deduction_np = int(round(np_income * np_rate))
    # 10원 단위 절사 for HI, LTI (matches Excel rounding pattern)
    deduction_hi = int(round(hi_income * hi_rate, 0)) // 10 * 10
    deduction_lti = int(round(deduction_hi * lti_multiplier, 0)) // 10 * 10
    # 고용보험: 보수월액 기준 (insurance_base가 있으면 보수월액, 없으면 gross_pay)
    deduction_ei = int(round(base_for_insurance * 0.009, 0)) // 10 * 10
    
    return {
        "np": deduction_np,
        "hi": deduction_hi,
        "lti": deduction_lti,
        "ei": deduction_ei
    }


