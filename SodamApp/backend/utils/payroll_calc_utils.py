def calculate_korean_income_tax(taxable_monthly_income: int, dependents: int = 1, children: int = 0) -> int:
    """
    Calculates Korean Income Tax based on the official 간이세액표 (Simplified Tax Table).
    Uses the 2024.03.01 시행 간이세액표 (valid through 2026).
    
    Args:
        taxable_monthly_income: 비과세 제외 월 급여액
        dependents: 공제대상 가족 수 (본인 포함, 1~11)
        children: 8세~20세 자녀 수
    
    Returns:
        Monthly income tax amount (소득세), 10원 단위 절사
    """
    if taxable_monthly_income < 1060000:
        return 0
    
    # Clamp dependents index (1-based to 0-based, max 11)
    dep_idx = max(0, min(dependents - 1, 10))
    
    # Look up in the table
    tax = 0
    found = False
    for (lower, upper), taxes in _SIMPLE_TAX_TABLE:
        if lower <= taxable_monthly_income < upper:
            tax = taxes[dep_idx]
            found = True
            break
    
    if not found:
        # For incomes above the table range, use the formula-based calculation
        # This handles very high incomes (550만원 이상)
        tax = _calculate_high_income_tax(taxable_monthly_income, dependents)
    
    # Child Tax Credit (8-20세 자녀)
    child_credit = 0
    if children == 1:
        child_credit = 12500
    elif children == 2:
        child_credit = 29160
    elif children >= 3:
        child_credit = 29160 + (children - 2) * 25000
    
    final_tax = max(0, tax - child_credit)
    
    # 10원 단위 절사
    return int(final_tax / 10) * 10


def _calculate_high_income_tax(income: int, dependents: int = 1) -> int:
    """
    Formula-based calculation for incomes above 550만원 (above the lookup table range).
    Uses the progressive tax rate structure from the simplified tax table methodology.
    """
    # Base tax at 5,500,000 for 1 dependent: ~311,780
    # Progressive rates for higher brackets
    if income <= 7000000:
        base_tax = 311780 + (income - 5500000) * 0.168
    elif income <= 10000000:
        base_tax = 563780 + (income - 7000000) * 0.248
    elif income <= 14000000:
        base_tax = 1307780 + (income - 10000000) * 0.350
    else:
        base_tax = 2707780 + (income - 14000000) * 0.380
    
    # Dependent deduction for high income
    if dependents >= 2:
        reduction_pct = min(0.80, (dependents - 1) * 0.15)
        base_tax = base_tax * (1 - reduction_pct)
    
    return int(base_tax / 10) * 10
