def classify_tax(name: str) -> str:
    """품명으로 과세/면세 판정. 반환: 'taxable' | 'tax_free'"""
    if not name:
        return "taxable"
    n = name.replace(" ", "")
    for kw in TAXABLE_OVERRIDES:
        if kw in n:
            return "taxable"
    for kw in TAX_FREE_KEYWORDS:
        if kw in n:
            return "tax_free"
    return "taxable"
